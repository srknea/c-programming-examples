#include <stdio.h>

// Print all natural numbers in reverse (from n to 1)*/

int main(){
	
	int n,i;
	
	printf("Bir adet dogal sayi girin: ");
	scanf("%d",&n);
	
	for(i=n;i>=0;i--)
	{
	printf("%d\n",i);	
	}
	
	return 0;
}

